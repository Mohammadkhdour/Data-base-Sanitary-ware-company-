package application;

public class Customers{
	
	public int Id;
	public String Name;
	
	public Customers(int id, String name) {
		super();
		this.Id = id;
		this.Name = name;
	}

	public int getId() {
		return Id;
	}

	public void setId(int id) {
		this.Id = id;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		this.Name = name;
	}
	
	
	
	
	
	
}